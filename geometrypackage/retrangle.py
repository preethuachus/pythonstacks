# circle.py for circle-related operations (area and circumference).
# rectangle.py for rectangle-related operations (area and perimeter).
# Use these modules in a program to calculate:
# The area and circumference of a circle (radius = 7).
#  The area and perimeter of a rectangle (length = 5, width = 3).

def area(l,w):
    return l*w

def perimeter(l,w):
    return 2*(l*w)